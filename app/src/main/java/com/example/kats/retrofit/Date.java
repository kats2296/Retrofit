public class Date {

    private String pretty;
    private String year;
    private String mon;
    private String mday	;
    private String hour;
    private String min;
    private String tzname;

    public String getPretty() {
        return pretty;
    }

    public String getYear() {
        return year;
    }

    public String getMon() {
        return mon;
    }

    public String getMday() {
        return mday;
    }

    public String getHour() {
        return hour;
    }

    public String getMin() {
        return min;
    }

    public String getTzname() {
        return tzname;
    }
}
